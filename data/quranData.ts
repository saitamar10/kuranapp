// Kıble açısı hesaplama fonksiyonu
export const calculateQiblaDirection = (userLat: number, userLng: number): number => {
  const kaabaLat = KAABA_COORDINATES.lat * (Math.PI / 180);
  const kaabaLng = KAABA_COORDINATES.lng * (Math.PI / 180);
  const userLatRad = userLat * (Math.PI / 180);
  const userLngRad = userLng * (Math.PI / 180);

  const y = Math.sin(kaabaLng - userLngRad);
  const x = Math.cos(userLatRad) * Math.tan(kaabaLat) - Math.sin(userLatRad) * Math.cos(kaabaLng - userLngRad);
  
  let qibla = Math.atan2(y, x) * (180 / Math.PI);
  qibla = (qibla + 360) % 360;
  
  return qibla;
};

